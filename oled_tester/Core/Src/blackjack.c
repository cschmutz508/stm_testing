void bjack_init() {
	ssd1306_Fill(Black);
	ssd1306_DrawBitmap(0, 0, BMP_empty_table, 128, 64, White);
	ssd1306_DrawBitmap(13, 13, BMP_empty_card, 9, 15, White);
	ssd1306_DrawBitmap(15, 15, BMP_card_num[0], 3, 5, White);
	ssd1306_DrawBitmap(15, 22, BMP_card_suit[3], 5, 5, White);

	ssd1306_UpdateScreen();
	HAL_Delay(3000);

	ssd1306_Fill(Black);
	ssd1306_DrawBitmap(0, 0, BMP_home_screen, 128, 64, White);
	uint8_t select = 0;

	bjack_update_menu(select);
	ssd1306_UpdateScreen();
}

void bjack_home() {
	ssd1306_Fill(Black);
	ssd1306_DrawBitmap(0, 0, BMP_home_screen, 128, 64, White);
	bool loop_c = true;
	uint8_t select = 0;

	bjack_update_menu(select);
	ssd1306_UpdateScreen();

	while (loop_c) {
		__WFI();
		if (b1_pressed) {
			b1_pressed = false;
			if (!(select == 0)) {
				select--;
				bjack_update_menu(select);
				ssd1306_UpdateScreen();
			}
		}
		if (b2_pressed) {
			b2_pressed = false;
			if (!(select == 2)) {
				select++;
				bjack_update_menu(select);
				ssd1306_UpdateScreen();
			}
		}
		if (b3_pressed) {
			b3_pressed = false;
			switch(select) {
			case 0: bjack_init(); break;
			case 1: break;
			case 2: break;
			}
		}
	}
}

void bjack_update_menu(uint8_t select) {
	ssd1306_FillRectangle(42, 37, 43, 38, Black);
	ssd1306_FillRectangle(42, 45, 43, 46, Black);
	ssd1306_FillRectangle(42, 53, 43, 54, Black);

	switch (select) {
	case 0: ssd1306_FillRectangle(42, 37, 43, 38, White); break;
	case 1: ssd1306_FillRectangle(42, 45, 43, 46, White); break;
	case 2: ssd1306_FillRectangle(42, 53, 43, 54, White); break;
	}

	ssd1306_UpdateScreen();
}

